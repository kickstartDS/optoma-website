// ─── Helpers ──────────────────────────────────────────────────────────

/**
 * Extract the component name from a JSON Schema `$id` URI.
 * E.g. `"http://…/hero.schema.json"` → `"hero"`.
 */
export function getSchemaName(schemaId: string): string {
  return (schemaId && schemaId.split("/").pop()?.split(".").shift()) || "";
}

// ─── Types ────────────────────────────────────────────────────────────

/** Options for `prepareSchemaForOpenAi`. */
export interface PrepareSchemaOptions {
  /**
   * Number of items to generate in the primary root array.
   * Sets both `minItems` and `maxItems` on that array.
   * (For page-like schemas this controls the section count.)
   */
  sections?: number;
  /**
   * Whitelist of component names to include.
   * Defaults to `SUPPORTED_COMPONENTS` (or `validationRules.allKnownComponents`
   * when validation rules are provided).
   */
  allowedComponents?: string[];
  /**
   * Property names to strip from every component schema.
   * Defaults to `DEFAULT_PROPERTIES_TO_DROP`.
   */
  propertiesToDrop?: string[];
  /**
   * Schema-derived validation rules from `buildValidationRules()`.
   * When provided, `allKnownComponents` is used as the default component
   * list and `subComponentMap` is used instead of the hardcoded
   * `SUB_COMPONENT_MAP`.
   */
  validationRules?: {
    allKnownComponents: Set<string>;
    subComponentMap: Record<string, string>;
    rootArrayFields?: string[];
    containerSlots?: Map<string, Set<string>>;
  };
  /**
   * Name of the primary root array field to apply `sections` (min/maxItems)
   * constraints to. When omitted, auto-detected from the schema's first
   * root-level array property or from `validationRules.rootArrayFields[0]`.
   */
  rootArrayField?: string;
  /**
   * Root-level property names to exclude from the prepared schema
   * (e.g. `["header", "footer", "seo"]`). These properties are deleted
   * from the cloned schema before OpenAI transformation.
   *
   * When omitted, auto-detected via `detectDefaultExcludes()`:
   * properties named `header`, `footer`, `token`, `seo` are excluded
   * by default.
   */
  rootPropertiesToExclude?: string[];
  /**
   * Content type name, used to set the envelope name
   * (e.g. `"blog-post"` → `"blog_post_response"`).
   * Defaults to `"page"`.
   */
  contentType?: string;
}

/** The OpenAI-compatible schema envelope. */
export interface OpenAiSchemaEnvelope {
  name: string;
  strict: boolean;
  schema: Record<string, any>;
}

/** Validation diagnostics returned alongside the prepared schema. */
export interface SchemaValidation {
  totalProperties: number;
  maxDepth: number;
  enumValueCount: number;
  warnings: string[];
}

/** Full result of `prepareSchemaForOpenAi`. */
export interface PreparedSchema {
  /** Schema envelope ready to pass to OpenAI `response_format.json_schema`. */
  envelope: OpenAiSchemaEnvelope;
  /**
   * Map of component type name → its original (pre-transformation) schema.
   * Used later by `processOpenAiResponse` to merge defaults.
   */
  schemaMap: Record<string, Record<string, any>>;
  /** Validation diagnostics. */
  validation: SchemaValidation;
}

// ─── Core API ─────────────────────────────────────────────────────────

/**
 * Transform a dereffed page schema into an OpenAI-compatible structured
 * output schema.
 *
 * This is the main entry point — it runs all 13 transformation passes
 * that were previously embedded in the PrompterComponent.
 *
 * @param pageSchema - The fully dereferenced page JSON Schema from
 *   `@kickstartds/ds-agency-premium/page/page.schema.dereffed.json`.
 * @param options - Optional configuration.
 * @returns The prepared schema envelope + component schema map.
 */
export function prepareSchemaForOpenAi(
  pageSchema: Record<string, any>,
  options: PrepareSchemaOptions = {}
): PreparedSchema {
  const {
    sections,
    propertiesToDrop = DEFAULT_PROPERTIES_TO_DROP as unknown as string[],
    validationRules,
  } = options;

  // Use schema-derived component list when available, fall back to hardcoded
  const defaultComponents: string[] = validationRules
    ? [...validationRules.allKnownComponents]
    : (SUPPORTED_COMPONENTS as unknown as string[]);
  const allowedComponents = options.allowedComponents || defaultComponents;

  // Use schema-derived sub-component map when available
  const subComponentMap: Record<string, string> = validationRules
    ? validationRules.subComponentMap
    : SUB_COMPONENT_MAP;

  // Mutable map that gets populated during traversal
  const schemaMap: Record<string, Record<string, any>> = {};

  // Validation accumulators
  const allProperties = new Set<string>();
  let maxDepth = 0;
  let enumValueCount = 0;
  const warnings: string[] = [];

  // ── Pass 0: Collect component schemas by type const ─────────────
  const collectSchemas: TraverseCallback = (schema) => {
    if (
      schema.properties &&
      schema.properties.type &&
      schema.properties.type.const
    ) {
      schemaMap[schema.properties.type.const] = structuredClone(schema);
    }
  };

  // Run on the *original* schema before cloning
  schemaTraverse(pageSchema, collectSchemas);

  // Clone so we don't mutate the caller's object
  const clonedSchema = structuredClone(pageSchema);

  // ── Structural pruning ──────────────────────────────────────────
  // Remove root-level properties that shouldn't be generated by AI
  const excludeProps =
    options.rootPropertiesToExclude || detectDefaultExcludes(clonedSchema);
  for (const prop of excludeProps) {
    if (clonedSchema.properties[prop]) {
      delete clonedSchema.properties[prop];
    }
  }

  // Determine the primary root array field for minItems/maxItems constraints
  const primaryArrayField =
    options.rootArrayField ||
    options.validationRules?.rootArrayFields?.[0] ||
    detectPrimaryRootArray(clonedSchema);

  if (
    sections !== undefined &&
    primaryArrayField &&
    clonedSchema.properties[primaryArrayField]
  ) {
    clonedSchema.properties[primaryArrayField].minItems = sections;
    clonedSchema.properties[primaryArrayField].maxItems = sections;
  }

  // Add type consts to sub-component arrays inside ALL root-level
  // polymorphic container slots (not just "section")
  const rootArrayFields =
    options.validationRules?.rootArrayFields ||
    detectRootArrayFields(clonedSchema);
  for (const arrayField of rootArrayFields) {
    const rootArray = clonedSchema.properties[arrayField];
    if (!rootArray?.items) continue;
    // Walk into the items to find polymorphic anyOf slots
    addSubComponentTypeConsts(rootArray.items, subComponentMap);
  }

  // ── Traverse-based transformation passes ────────────────────────

  const addTypeConsts: TraverseCallback = (schema) => {
    if (schema && schema.anyOf) {
      schema.anyOf.forEach((component: any) => {
        if (component.$id) {
          const componentName = getSchemaName(component.$id);
          if (
            allowedComponents.includes(componentName) &&
            !component.properties?.type
          ) {
            component.properties.type = { const: componentName };
          }
        }
      });
    }
  };

  const filterComponents: TraverseCallback = (schema) => {
    if (schema && schema.anyOf) {
      schema.anyOf = schema.anyOf.filter((component: any) => {
        return (
          component.properties?.type &&
          component.properties.type.const &&
          allowedComponents.includes(component.properties.type.const)
        );
      });
    }
  };

  const deleteConsts: TraverseCallback = (
    schema,
    jsonPtr,
    _rootSchema,
    _parentJsonPtr,
    parentKeyword,
    parentSchema
  ) => {
    if (schema.const && parentKeyword === "properties" && parentSchema) {
      const propName = jsonPtr.split("/").pop();
      if (propName === "type") {
        const type: Record<string, any> = { properties: {} };
        type.properties[`type__${schema.const}`] = {
          type: "string",
          title: "Type of component",
          description: `A field always being set to the value '${schema.const}'`,
        };
        parentSchema.properties = {
          ...type.properties,
          ...parentSchema.properties,
        };
      }
      if (propName) {
        delete parentSchema.properties[propName];
      }
    }
  };

  const removeImageFormatProperties: TraverseCallback = (
    schema,
    _jsonPtr,
    _rootSchema,
    _parentJsonPtr,
    _parentKeyword,
    _parentSchema,
    _keyIndex
  ) => {
    if (schema.format === "image" || schema.format === "video") {
      delete schema.format;
    }
  };

  const removeIconProperties: TraverseCallback = (
    _schema,
    _jsonPtr,
    _rootSchema,
    _parentJsonPtr,
    _parentKeyword,
    parentSchema,
    keyIndex
  ) => {
    if (keyIndex === "icon" && parentSchema?.properties?.[keyIndex]) {
      delete parentSchema.properties[keyIndex];
    }
  };

  const removeUnsupportedProperties: TraverseCallback = (
    _schema,
    _jsonPtr,
    _rootSchema,
    _parentJsonPtr,
    _parentKeyword,
    parentSchema,
    keyIndex
  ) => {
    if (
      keyIndex &&
      typeof keyIndex === "string" &&
      propertiesToDrop.includes(keyIndex) &&
      parentSchema?.properties?.[keyIndex]
    ) {
      delete parentSchema.properties[keyIndex];
    }
  };

  const removeUnsupportedKeywords: TraverseCallback = (schema) => {
    for (const key of UNSUPPORTED_KEYWORDS) {
      if (Object.prototype.hasOwnProperty.call(schema, key)) {
        delete schema[key];
      }
    }
  };

  const removeEmptyObjects: TraverseCallback = (
    schema,
    _jsonPtr,
    _rootSchema,
    _parentJsonPtr,
    _parentKeyword,
    parentSchema,
    keyIndex
  ) => {
    if (
      schema.type === "object" &&
      schema.properties &&
      !Object.keys(schema.properties).length &&
      keyIndex &&
      parentSchema?.properties?.[keyIndex]
    ) {
      delete parentSchema.properties[keyIndex];
    }
  };

  const denyAdditionalProperties: TraverseCallback = (schema) => {
    if (schema.type === "object") {
      schema.additionalProperties = false;
    }
  };

  const makeRequired: TraverseCallback = (schema) => {
    if (schema.type === "object" && schema.properties) {
      schema.required = Object.keys(schema.properties);
    }
  };

  // ── Validation passes ───────────────────────────────────────────

  const collectProperties: TraverseCallback = (
    _schema,
    jsonPtr,
    _rootSchema,
    _parentJsonPtr,
    parentKeyword
  ) => {
    if (
      jsonPtr &&
      parentKeyword === "properties" &&
      !allProperties.has(jsonPtr)
    ) {
      allProperties.add(jsonPtr);
    }
  };

  const countDepth: TraverseCallback = (_schema, jsonPtr) => {
    const depth = jsonPtr.split("/properties/").length;
    maxDepth = Math.max(depth, maxDepth);
  };

  const countEnums: TraverseCallback = (schema) => {
    if (schema.enum) {
      enumValueCount += schema.enum.length;
    }
  };

  // ── Execute all passes ──────────────────────────────────────────
  [
    addTypeConsts,
    filterComponents,
    deleteConsts,
    removeImageFormatProperties,
    removeIconProperties,
    removeUnsupportedProperties,
    removeUnsupportedKeywords,
    removeEmptyObjects,
    denyAdditionalProperties,
    makeRequired,
    collectProperties,
    countDepth,
    countEnums,
  ].forEach((fn) => {
    schemaTraverse(clonedSchema, fn);
  });

  // ── Collect warnings ────────────────────────────────────────────
  if (allProperties.size > 5000) {
    warnings.push(
      `Property count (${allProperties.size}) exceeds OpenAI limit of 5000.`
    );
  }
  if (enumValueCount > 1000) {
    warnings.push(
      `Enum value count (${enumValueCount}) exceeds OpenAI limit of 1000.`
    );
  }

  const contentType = options.contentType || "page";
  const envelopeName = `${contentType.replace(/-/g, "_")}_response`;

  return {
    envelope: {
      name: envelopeName,
      strict: true,
      schema: clonedSchema,
    },
    schemaMap,
    validation: {
      totalProperties: allProperties.size,
      maxDepth,
      enumValueCount,
      warnings,
    },
  };
}

/**
 * Derive a single-component OpenAI-ready schema from the full page schema.
 *
 * This is useful for generating content for one specific component type
 * (e.g. "hero", "faq") without needing the full page envelope.
 *
 * Searches all polymorphic container slots (not just `section.components`)
 * via `ValidationRules.containerSlots` when available.
 *
 * @param pageSchema - The fully dereferenced page schema.
 * @param componentName - Name of the component (e.g. "hero").
 * @param options - Optional configuration including validationRules.
 * @returns A single-component schema envelope, or `null` if the component
 *   is not found.
 */
export function getComponentPresetSchema(
  pageSchema: Record<string, any>,
  componentName: string,
  options?: PrepareSchemaOptions
): OpenAiSchemaEnvelope | null {
  const { envelope } = prepareSchemaForOpenAi(pageSchema, {
    ...options,
    allowedComponents: [
      componentName,
      ...(options?.allowedComponents || ["section"]),
    ],
    sections: options?.sections || 1,
  });

  // Search all polymorphic slots for the target component
  const match = findComponentInSchema(envelope.schema, componentName);
  if (!match) return null;

  // Return a standalone schema for just that component
  return {
    name: `${componentName.replace(/-/g, "_")}_section`,
    strict: true,
    schema: match,
  };
}

/**
 * Return the list of component names available for AI generation.
 */
export function listAvailableComponents(): string[] {
  return [...SUPPORTED_COMPONENTS];
}

// ─── Internal helpers ─────────────────────────────────────────────────

/**
 * Auto-detect root-level properties that should be excluded from AI
 * generation. Properties named `header`, `footer`, `token`, `seo` are
 * typically layout / meta concerns that should not be generated.
 */
function detectDefaultExcludes(schema: Record<string, any>): string[] {
  const DEFAULT_EXCLUDE_NAMES = ["header", "footer", "token", "seo"];
  const props = schema.properties || {};
  return DEFAULT_EXCLUDE_NAMES.filter((name) => name in props);
}

/**
 * Find the first root-level array property in a schema.
 * Used as fallback when `rootArrayField` and `validationRules` aren't provided.
 */
function detectPrimaryRootArray(schema: Record<string, any>): string | null {
  const props = schema.properties || {};
  for (const [key, value] of Object.entries<any>(props)) {
    if (value && value.type === "array" && value.items) {
      return key;
    }
  }
  return null;
}

/**
 * Detect all root-level array field names from a schema.
 * Used as fallback when `validationRules.rootArrayFields` isn't provided.
 */
function detectRootArrayFields(schema: Record<string, any>): string[] {
  const fields: string[] = [];
  const props = schema.properties || {};
  for (const [key, value] of Object.entries<any>(props)) {
    if (
      value &&
      value.type === "array" &&
      value.items &&
      typeof value.items === "object"
    ) {
      fields.push(key);
    }
  }
  return fields;
}

/**
 * Recursively add `type` const properties to sub-component arrays
 * inside a container's items schema.
 *
 * This replaces the old hardcoded `section.items.properties.components.items`
 * path with a generic walk that finds all polymorphic `anyOf` slots.
 */
function addSubComponentTypeConsts(
  itemsSchema: Record<string, any>,
  subComponentMap: Record<string, string>
): void {
  if (!itemsSchema || typeof itemsSchema !== "object") return;

  const properties = itemsSchema.properties || {};

  for (const [_propKey, propValue] of Object.entries<any>(properties)) {
    if (!propValue || typeof propValue !== "object") continue;

    // Look for array properties with polymorphic items (anyOf)
    if (propValue.type === "array" && propValue.items?.anyOf) {
      for (const componentSchema of propValue.items.anyOf) {
        const componentName = componentSchema.$id
          ? getSchemaName(componentSchema.$id)
          : "";
        if (!componentName) continue;

        const arrayKey = subComponentMap[componentName];
        if (
          arrayKey &&
          componentSchema.properties?.[arrayKey]?.items?.properties
        ) {
          componentSchema.properties[arrayKey].items.properties.type = {
            const: arrayKey,
          };
        }
      }
    }
  }
}

/**
 * Search a prepared schema for a component by name in any polymorphic
 * `anyOf` slot. Returns the matching schema node or `null`.
 */
function findComponentInSchema(
  schema: Record<string, any>,
  componentName: string
): Record<string, any> | null {
  const props = schema.properties || {};

  for (const [_key, value] of Object.entries<any>(props)) {
    if (!value || typeof value !== "object") continue;

    // Check array items with anyOf
    if (value.type === "array" && value.items) {
      const items = value.items;

      // Direct anyOf on items
      if (items.anyOf) {
        const match = items.anyOf.find((s: any) => {
          const typeKey = Object.keys(s.properties || {}).find((k: string) =>
            k.startsWith("type__")
          );
          return typeKey === `type__${componentName}`;
        });
        if (match) return match;
      }

      // Nested: items.properties.*.items.anyOf (like section.components)
      if (items.properties) {
        for (const [_nestedKey, nestedValue] of Object.entries<any>(
          items.properties
        )) {
          if (nestedValue?.type === "array" && nestedValue?.items?.anyOf) {
            const match = nestedValue.items.anyOf.find((s: any) => {
              const typeKey = Object.keys(s.properties || {}).find(
                (k: string) => k.startsWith("type__")
              );
              return typeKey === `type__${componentName}`;
            });
            if (match) return match;
          }
        }
      }
    }
  }

  return null;
}
